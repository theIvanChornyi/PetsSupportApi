const mongoose = require('mongoose');

const noticeArrModel = new mongoose.Schema(
  {
    id: {
      type: Array,
        },
    favorite: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'favorite',
    },
  },
  { versionKey: false }
);


const NoticeArr = mongoose.model('noticeArr', noticeArrModel);

module.exports = NoticeArr;
